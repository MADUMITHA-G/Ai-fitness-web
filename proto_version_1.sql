-- Register table
create table register (
    user_id int primary key auto_increment,
    name varchar(50) not null,
    age int check (age > 0),
    gender varchar(10),
    mail_id varchar(100) unique not null,
    password varchar(100) not null,
    height decimal(5,2), 
    weight decimal(5,2), 
    bmi decimal(5,2)      
);

